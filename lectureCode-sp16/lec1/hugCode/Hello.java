/* Every file in Java must define a class
 * with the same name as that file. */
public class Hello {
	public static void main(String[] args) {
		System.out.println("hello world");
		/* Before a variable can be used in
		Java, it must be defined and given a
		type. Types are checked before
		a program can even run. */
		int x;

		x = 30;
		x = 2 * x;
		System.out.println(x);

		x = "ross is a sadist";
		System.out.println(x);
	}
}

/*
Statements must end in semicolons.
*/